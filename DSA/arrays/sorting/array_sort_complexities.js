/* 
 here's a list of standard time complexities for various sorting algorithms in terms of best case, average case, and worst case scenarios:

1.Bubble Sort :- Best Case: O(n)
                 Average Case: O(n^2)
                 Worst Case: O(n^2) 
2.Selection Sort:- Best Case: O(n^2)
                   Average Case: O(n^2)
                   Worst Case: O(n^2)
3.Insertion Sort:-Best Case: O(n)
                  Average Case: O(n^2)
                  Worst Case: O(n^2) 
4.Merge Sort:- Best Case: O(n log n)
               Average Case: O(n log n)
               Worst Case: O(n log n)
5.Quick Sort:-Best Case: O(n log n)
              Average Case: O(n log n)
              Worst Case: O(n^2) (rare)
6.Heap Sort:- Best Case: O(n log n)
              Average Case: O(n log n)
            Worst Case: O(n log n)
7.Counting Sort:- Best Case: O(n + k)
                  Average Case: O(n + k)
                  Worst Case: O(n + k)
8.Radix Sort:- Best Case: O(nk)
               Average Case: O(nk)
               Worst Case: O(nk)
Note:

n represents the number of elements in the array.
k represents the range of input (for Counting Sort and Radix Sort).
The time complexities mentioned above are based on comparisons between elements, except for Counting Sort and Radix Sort, which are non-comparison based algorithms.

*/
